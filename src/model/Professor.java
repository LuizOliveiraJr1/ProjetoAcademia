package model;

import java.util.Date;

/**
 *
 * @author Luiz Carlos
 */
public class Professor {
   
    private int idProfessor;
    private String nome;
    private String sexo;
    private String cidade;
    private String endereco;
    private String telefone;
    private String tuno;
    private Date dataMatriculs;
    private String bairro;
    private String celular;

    public int getIdProfessor() {
        return idProfessor;
    }

    public void setIdProfessor(int idProfessor) {
        this.idProfessor = idProfessor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getTuno() {
        return tuno;
    }

    public void setTuno(String tuno) {
        this.tuno = tuno;
    }

    public Date getDataMatriculs() {
        return dataMatriculs;
    }

    public void setDataMatriculs(Date dataMatriculs) {
        this.dataMatriculs = dataMatriculs;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    
}
